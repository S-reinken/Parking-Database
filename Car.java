package main;


class Car
{
	String licenseplate;
	String owner;
    String year;
    String color;
    String make;
    String model;
    
    public void setYear(String a)
    {
    	year = a;
    }
    
    public void setLicensePlate(String a)
    {
    	licenseplate = a;
    }
    
    public void setOwner(String a)
    {
    	owner = a;
    }
    
    public void setColor(String a)
    {
    	color = a;
    }
    
    public void setMake(String a)
    {
    	make = a;
    }
    
    public void setModel(String a)
    {
    	model = a;
    }
}

